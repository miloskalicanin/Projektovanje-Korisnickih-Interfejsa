    private void leaveRatingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leaveRatingActionPerformed
        String ratingText = Utils.inputTextDialog(this, "Slanje ocene", "Unesite ocenu od 1 do 5");
        Double rate = null;
        try {
            rate = Double.parseDouble(ratingText);
            if (rate < 1 || rate > 5) {
                throw new Exception("Invalid rate value");
            }
        } catch (Exception e) {
            Utils.displayErrorDialog(this, "Slanje ocene", "Format unete ocene nije validan.");
            return;
        }

        try {
            MockDatabaseService.getInstance().sendRate(requestInfo, rate);
            requestInfo.setRateSent(true);

            leaveRating.setEnabled(false);
            leaveRating.setBackground(Color.DARK_GRAY);
            Utils.displaySuccessDialog(this, "Slanje ocene", "Slanje ocene je uspesno!");
        } catch (Exception ex) {
            Utils.displayErrorDialog(this, "Slanje ocene", "Slanje ocene nije uspesno.");
        }
    }//GEN-LAST:event_leaveRatingActionPerformed

