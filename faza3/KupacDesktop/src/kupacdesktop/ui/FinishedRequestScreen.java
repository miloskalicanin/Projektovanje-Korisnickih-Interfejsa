    private void leaveCommentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leaveCommentActionPerformed
        String comment = Utils.inputTextDialog(this, "Slanje komentara", "Unesite komentar");
        if (comment == null || comment.isEmpty()) {
            Utils.displayErrorDialog(this, "Slanje komentara", "Neispravan format komentara.");
            return;
        }

        try {
            MockDatabaseService.getInstance().sendComment(requestInfo, comment);
            requestInfo.setCommentSent(true);

            leaveComment.setEnabled(false);
            leaveComment.setBackground(Color.DARK_GRAY);
            Utils.displaySuccessDialog(this, "Slanje komentara", "Slanje komentara je uspesno!");
        } catch (Exception ex) {
            Utils.displayErrorDialog(this, "Slanje komentara", "Slanje komentara nije uspesno.");
        }
    }//GEN-LAST:event_leaveCommentActionPerformed

    private void setupScreen() {
        UserDTO repairman = requestInfo.getRepairman();
        labelFirstAndLastName.setText(repairman.getFirstName() + " " + repairman.getLastName());
        labelAddress.setText(repairman.getAddress());
        labelPhone.setText(repairman.getPhoneNumber());
        labelExperience.setText(repairman.getExperience() + " godina");
        labelPrice.setText(repairman.getPrice() + " din");
        labelJob.setText(repairman.getJob());
        labelRating.setText(String.format("%.2f", repairman.getRating()));

        if (repairman.getSpecialTechniques() != null) {
            List<String> techniques = new LinkedList<>();
            for (String t : repairman.getSpecialTechniques()) {
                String[] arr = t.split(",");
                for (String tt : arr) {
                    techniques.add(tt.replaceAll(" ", ""));
                }
            }
            specTechPanel.setLayout(new GridLayout(techniques.size(), 1));
            for (String technique : techniques) {
                JLabel label = new JLabel(technique, JLabel.CENTER);
                //label.setBorder(BorderFactory.createLineBorder(Color.BLACK));
                specTechPanel.add(label);
            }
        } else {
            specTechPanel.setLayout(new GridLayout());
        }

        SimpleDateFormat format = new SimpleDateFormat("dd.MM.yyyy.");
        String dateBegin = format.format(requestInfo.getDateBegin());
        String dateEnd = format.format(requestInfo.getDateEnd());

        labelDateInterval.setText(dateBegin + " - " + dateEnd);
        labelPayment.setText(requestInfo.getPaymentMethod());

        if (requestInfo.isCommentSent()) {
            leaveComment.setEnabled(false);
            leaveComment.setBackground(Color.DARK_GRAY);
        }
        if (requestInfo.isRateSent()) {
            leaveRating.setEnabled(false);
            leaveRating.setBackground(Color.DARK_GRAY);
        }
    }


