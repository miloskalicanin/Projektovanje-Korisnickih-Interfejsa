package rs.etf.majstorandroid.recycler;

/**
 * Enumeration of item types supported by recycler list.
 */
public class RecyclerItemType {
    /**
     * Item with data
     */
    public static final int ITEM = 1;
    /**
     * Item shown when there is no data in recycler list
     */
    public static final int EMPTY = 2;
}
